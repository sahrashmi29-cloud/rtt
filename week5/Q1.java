public class Q1
{
    public static void main(String[] args)
    {
        // for loop
      for(int i=2;i<=10;i+=2)
      {
        System.out.println(i);
      }
      
      // while loop
      int i=2;
      while(i<=10)
      {
        System.out.println(i);
        i+=2;
      }
      
      // do while
      int k=2;
      do{
          System.out.println(k);
          k+=2;
    }
    while(k<=10);
    }
}